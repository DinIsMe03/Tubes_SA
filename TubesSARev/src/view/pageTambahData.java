    public void setDataTable(){
        DefaultTableModel tableModel = (DefaultTableModel) tblDataBarang.getModel();
        tokoBangunan toko = new tokoBangunan();
        tblDataBarang.setFont(new Font("Tahoma", Font.PLAIN, 14));
        
        for(int i = 0; i < toko.getTokoBangunan().size(); i++){
            String nama = toko.getDataBangunan(i).getBarang();
            String kategori = toko.getDataBangunan(i).getKategori();
            int harga = toko.getDataBangunan(i).getHarga();
            int kualitas = toko.getDataBangunan(i).getKualitas();
            
            Object[] data = {nama, kategori, harga, kualitas};
            
            tableModel.addRow(data);
        }
    }
    
