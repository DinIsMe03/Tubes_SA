    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        
        if(txtFieldHargaBarang.getText() != "" || txtFieldNamaBarang.getText() != ""){
            String namaBarang = txtFieldNamaBarang.getText();
            String kategoriBarang = (String) comboBoxKategoriBarang.getSelectedItem();
            int hargaBarang = Integer.parseInt(txtFieldHargaBarang.getText());
            int kualitasBarang = Integer.valueOf((String) comboBoxKualitasBarang.getSelectedItem());

            if(comboBoxToko.getSelectedIndex() == 0){
                tokoBangunan toko = new tokoBangunan();
                dataBangunan data = new dataBangunan();
                DefaultTableModel model = (DefaultTableModel) tblDataBarang.getModel();
                
                data = data.setDataBarang(namaBarang, kategoriBarang, hargaBarang, kualitasBarang);
                
                toko.addDataBangunan(data);
                
                switch(comboBoxKategoriBarang.getSelectedIndex()){
                    case 0:
                        toko.addJenisBangunan(data);
                        break;
                    case 1:
                        toko.addJenisLantai(data);
                        break;
                    case 2:
                        toko.addJenisPerekat(data);
                        break;
                    case 3:
                        toko.addJenisAdonan(data);
                        break;
                    case 4:
                        toko.addJenisAtap(data);
                        break;
                }
                
                model.setRowCount(0);
                setDataTable();
            }
        }
    }//GEN-LAST:event_btnSimpanActionPerformed


